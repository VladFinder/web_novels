export const getEmotionIcon = (id) => getEmotionMeta(id).icon || defaultIcon;
export const getMiniEmotionIcon = (id) => getEmotionMeta(id).miniIcon || defaultIcon;
export const getEmotionTheme = (id) => getEmotionMeta(id).theme || defaultTheme;
